int	ft_sqrt(int nb)
{
	int i;
	int half;

	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	half = nb / 2;
	i = 1;
	while (i <= half && i <= 46340)
	{
		if (i * i == nb)
			return (i);
		i++;
	}
	return (0);
}
