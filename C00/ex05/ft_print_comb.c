void	ft_print_comb(void)
{
	char a;
	char b;
	char c;

	a = '/';
	b = '0';
	c = '0';
	while (a++ <= '6')
	{
		b = a + 1;
		while (b <= '8')
		{
			c = b + 1;
			while (c <= '9')
			{
				write(1, &a, 1);
				write(1, &b, 1);
				write(1, &c, 1);
				write(1, ", ", 2);
				c++;
			}
			b++;
		}
	}
	write(1, "789", 3);
}
